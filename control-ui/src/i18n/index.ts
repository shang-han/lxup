const translations: Record<string, any> = { en, 'zh-CN': zhCN };

class I18n {
  locale = 'zh-CN';
  subscribers = new Set<() => void>();

  constructor() {
    this._readStored();
  }

  _readStored() {
    try {
      const v = localStorage.getItem('openclaw.i18n.locale');
      if (v && translations[v]) this.locale = v;
    } catch { }
  }

  t(path: string, vars?: Record<string, any>): string {
    const keys = path.split('.');
    let val: any = translations[this.locale] || en;
    for (const k of keys) val = val?.[k];
    if (typeof val !== 'string') return path;
    if (vars) {
      let s = val;
      for (const [k, v] of Object.entries(vars)) s = s.replace(`{${k}}`, String(v));
      return s;
    }
    return val;
  }

  get(path: string): any {
    const keys = path.split('.');
    let val: any = translations[this.locale] || en;
    for (const k of keys) val = val?.[k];
    return val;
  }

  setLocale(code: string) {
    if (!translations[code]) return;
    this.locale = code;
    try { localStorage.setItem('openclaw.i18n.locale', code); } catch { }
    for (const fn of this.subscribers) fn();
  }

  subscribe(fn: () => void) {
    this.subscribers.add(fn);
    return () => { this.subscribers.delete(fn); };
  }
}

export const i18n = new I18n();
export const L = (path: string, vars?: Record<string, any>) => i18n.t(path, vars);
export const Lget = (path: string) => i18n.get(path);
